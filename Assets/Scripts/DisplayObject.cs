public class DisplayObject {
	private string _name;
	private float _x;
	private float _y;
	private float _width;
	private float _height;

	public string name {
		get { return _name; }
		set { _name = value; }
	}

	public float x {
		get { return _x; }
		set { _x = value; }
	}
	

	public float y {
		get { return _y; }
		set { _y = value; }
	}

	public float width {
		get { return _width; }
		set { _width = value; }
	}

	public float height {
		get { return _height; }
		set { _height = value; }
	}
}
